x = 6
y = 2

print(f" {x} mas {y} es igual a {x+y}")
print(f" {x} menos {y} es igual a {x-y}")
print(f" {x} multiplicado por {y} es igual a {x*y}")
print(f" {x} dividido por {y} es igual a {x/y}")

print(f" {x} dividido al piso {y} es igual a {x//y}")
print(f" {x} modulo de {y} es igual a {x%y}")
print(f" {x} elevado a {y} es igual a {x**y}")
print(f" la raiz cuadrada de {x} es igual a {x**0}")